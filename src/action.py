def main(topic, categories, interest, threshold):
    """
    interest = "
    1. Large language model pretraining and finetunings
    2. Multimodal machine learning
    3. Do not care about specific application, for example, information extraction, summarization, etc.
    4. Not interested in paper focus on specific languages, e.g., Arabic, Chinese, etc.\n"
    """
    if topic == "Physics":
        raise RuntimeError("You must choose a physics subtopic.")
    elif topic in physics_topics:
        abbr = physics_topics[topic]
    elif topic in topics:
        abbr = topics[topic]
    else:
        raise RuntimeError(f"Invalid topic {topic}")
    if categories:
        for category in categories:
            if category not in category_map[topic]:
                raise RuntimeError(f"{category} is not a category of {topic}")
        papers = get_papers(abbr)
        papers = [
            t
            for t in papers
            if bool(set(process_subject_fields(t["subjects"])) & set(categories))
        ]
    else:
        papers = get_papers(abbr)

    contents = []
    if interest:
        relevancy = get_top_relevance_paper(
            papers,
            query=interest,
            threshold_score=threshold,
            num_paper_in_prompt=16,
        )

        for paper in relevancy:
            pdf = paper["pdf"]
            title = paper["title"]

            title_slug = title.lower().replace(" ", "_")
            contents.append({
                "title": title, 
                "content": read_paper(title_slug, pdf),
                "authors": paper["authors"], 
                "subjects": paper["subjects"], 
                "abstract": paper["abstract"],
                "main_page": paper["main_page"],
                "pdf": paper["pdf"],
            })
    else:
        for paper in papers:
            pdf = paper["pdf"]
            title = paper["title"]

            title_slug = title.lower().replace(" ", "_")
            contents.append({
                "title": title, 
                "content": read_paper(title_slug, pdf),
                "authors": paper["authors"], 
                "subjects": paper["subjects"], 
                "abstract": paper["abstract"],
                "main_page": paper["main_page"],
                "pdf": paper["pdf"],
            })
        
    summaries = summarize(contents)
    body = "<br><br>".join(
        [
            f'Title: <a href="{paper["main_page"]}">{paper["title"]}</a><br>Authors: {paper["authors"]}<br>Subjects: {paper["subjects"]}<br>Summary: {paper["summary"]}'
            for paper in contents
        ]
    )
    podcast_file_name, transcript_file_name = generate_podcast(summaries)
    return podcast_file_name, transcript_file_name, body




if __name__ == "__main__":
    # Load the .env file.
    load_dotenv()
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--config", help="yaml config file to use", default="config.yaml"
    )
    args = parser.parse_args()
    with open(args.config, "r") as f:
        config = yaml.safe_load(f)

    if "OPENAI_API_KEY" not in os.environ:
        raise RuntimeError("No openai api key found")
    openai.api_key = os.environ.get("OPENAI_API_KEY")

    topic = config["topic"]
    categories = config["categories"]
    from_email = os.environ.get("FROM_EMAIL")
    to_email = os.environ.get("TO_EMAIL")
    threshold = config["threshold"]
    interest = config["interest"]
    podcast_file_name, transcript_file_name, body = main(topic, categories, interest, threshold)
    with open("digest.html", "w") as f:
        f.write(body)
    if os.environ.get("SENDGRID_API_KEY", None):
        sg = SendGridAPIClient(api_key=os.environ.get("SENDGRID_API_KEY"))
        from_email = Email(from_email)  # Change to your verified sender
        to_email = To(to_email)
        subject = date.today().strftime("Personalized arXiv Digest, %d %b %Y")
        content = Content("text/html", body)
        mail = Mail(from_email, to_email, subject, content)
        mail_json = mail.get()

        # Send an HTTP POST request to /mail/send
        response = sg.client.mail.send.post(request_body=mail_json)
        if response.status_code >= 200 and response.status_code <= 300:
            print("Send test email: Success!")
        else:
            print("Send test email: Failure ({response.status_code}, {response.text})")
    else:
        print("No sendgrid api key found. Skipping email")